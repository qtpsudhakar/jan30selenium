package com.java.basics;

public class C1Variables {

	public static void main(String[] args) {
		
		//datatypes
		
		//byte range is -128 - 127
		//-2 to the power of 7 to 2 to the power of 7 -1
				
		byte b =120;
		System.out.println(b);

		//short
		//-2 to the power of 15 to 2 to the power of 15 -1				
		short s =32000;
		System.out.println(s);

		//int
		//-2 to the power of 31 to 2 to the power of 31 -1				
		int i = 2140000000;
		System.out.println(i);

		//long
		//-2 to the power of 63 to 2 to the power of 63 -1
		//when assigning value you must sufix it with L
		
		//you can use it for storing currency/population
		long ln = 2150000000L;
		System.out.println(ln);

		//float
		//32 bit: can store 6-7 floting points
		//when assigning value you must sufix it with F
		float f = 2.123456789f;
		System.out.println(f);
		
		//double
		//64bit: can store 15-16 floating points
		double d = 2.123456789123456789;
		System.out.println(d);
		
		//char
		//16 bit: can store a single character
		
		char c = 'x';
		System.out.println(c);
		
		//boolean
		//8bit:1bit: true or false
		boolean bl = true;
		System.out.println(bl);
	}

}








