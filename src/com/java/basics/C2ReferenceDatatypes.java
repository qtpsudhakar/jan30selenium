package com.java.basics;

public class C2ReferenceDatatypes {

	public static void main(String[] args) {
		
		//storing string
		String str = "selenium";
		System.out.println(str);

		String str1 = "10";
		System.out.println(str1);
		String str2 = "20";
		System.out.println(str1+str2);
		
		Object obj = 10;
		System.out.println(obj);
		obj = "webdriver";
		System.out.println(obj);
		obj = 1.1;
		System.out.println(obj);
		
	}

}





